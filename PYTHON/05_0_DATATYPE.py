#                        Built-in Data Types


# Variables can store data of different types.

# Python has the following datatypes built-in by default :

        # Numeric Types   :	int, float, complex

        # Text Type       :	str (no char in python)
        
        # Boolean Type    :	bool
        
        # Sequence Types  :	list[], tuple(), range() --no array--
        
        # Mapping Type    :	dict{key:value}

        # Set Types       :	set{}, frozenset
        
        # Binary Types    :	bytes, bytearray, memoryview
        
        # None Type       :	NoneType

# --------------------------------------------------------------------
#                    Getting the Data Type

# You can get the data type of any object by using the type() function:

a = "a"
b = 1
c = 0.5
d = 1j
e = ["list"]

print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
