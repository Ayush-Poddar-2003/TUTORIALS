# NUMERIC DATATYPE

# There are three numeric types in Python:

#   Int - whole number, positive or negative, without decimals, unlimited length.
#   Float - positive or negative, one or more decimals, e to indicate the power of 10.
#   Complex - written with "j" as the imaginary part:

x = 1    # int
y = 2.8  # float
z = 1j   # complex

print(type(x))
print(type(y))
print(type(z))

# NOTE : You can't typecast COMPLEX into other data types.
